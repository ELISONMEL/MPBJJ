        <h1>Venha fazer parte da Familia MPBJJ - NOVA UNIÃO</h1>
        <p>Nova União é o resultado da fusão de duas equipes, Dedé Pederneiras Jiu Jitsu e Mello Tenis Clube, duas academias do Rio de Janeiro, lideradas respectivamente por André Pederneiras e Wendell Alexander. Pederneiras e Alexander se conheceram num campeonato de jiu jitsu onde ambos tiveram seus times competiram, por diversas ocasiões eles se encontraram em campeonartos seguintes e se tornaram bons amigos. No começo de 1995, Wendell teve um atleta inscrito em um torneio menor no qual não iria poder estar presente, na ocasião ele perguntou para o Pederneiras se poderia ajudar seu atleta quando da sua ausência. Esta foi a primeira vez que um competidor da equipe de Alexander lutou para Dedé. 3 meses depois, foi Pederneiras que não poderia estar presente em numa competicão e seus lutadores lutaram pela equipe de Alexander. Depois disso, os 2 chegaram à conclusão de que eram mais fortes quando trabalhavam em equipe, e que eles poderiam fazer frente à times de grande porte nos torneios mais importantes, assim decidindo pela fusão que passariam a chamar de Nova União.</p>
        
